package com.aca;

public class IntExtremes {
    public static void main(String[] args) {
        int positiveInt = Integer.MAX_VALUE;
        System.out.println(positiveInt);

        positiveInt++;
        System.out.println(positiveInt);

        int negativeInt = Integer.MIN_VALUE;
        System.out.println(negativeInt);

        negativeInt--;
        System.out.println(negativeInt);
    }
}
